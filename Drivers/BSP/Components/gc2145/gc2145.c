/**
  * @brief  Control GC2145 camera mirror/vflip.
  * @param  pObj  pointer to component object
  * @param  Config To configure mirror, flip, both or none
  * @retval Component status
  */
int32_t GC2145_SetMirrorFlipConfig(GC2145_Object_t *pObj, uint32_t Config)
{
  int32_t ret;
  uint8_t val;

  if (pObj == NULL)
  {
    ret = GC2145_ERROR;
  }
  else
  {
    if (gc2145_read_reg(&pObj->Ctx, GC2145_RESET_RELATED, (uint8_t *)&val, 1) != GC2145_OK)
    {
      ret = GC2145_ERROR;
    }
    else
    {
      val &= 0xFCU;  /* Set Register Page = 0 */
      if (gc2145_write_reg(&pObj->Ctx, GC2145_RESET_RELATED, (uint8_t *)&val, 1) != GC2145_OK)
      {
        ret = GC2145_ERROR;
      }
      else
      {
        ret = GC2145_OK;
      }
    }

    if (ret == GC2145_OK)
    {
      if (gc2145_read_reg(&pObj->Ctx, GC2145_ANALOG_MODE_1, (uint8_t *)&val, 1) != GC2145_OK)
      {
        ret = GC2145_ERROR;
      }
      else
      {
        val &= 0xFCU;

        switch (Config)
        {
          case GC2145_FLIP :
            val |= 0x02U;
            break;
          case GC2145_MIRROR:
            val |= 0x01U;
            break;
          case GC2145_MIRROR_FLIP :
            val |= 0x03U;
            break;
          case GC2145_MIRROR_FLIP_NONE:
          default:
            break;
        }

        if (gc2145_write_reg(&pObj->Ctx, GC2145_ANALOG_MODE_1, (uint8_t *)&val, 1) != GC2145_OK)
        {
          ret = GC2145_ERROR;
        }
        else
        {
          ret = GC2145_OK;
        }

      }
    }

  }

  return ret;
}

