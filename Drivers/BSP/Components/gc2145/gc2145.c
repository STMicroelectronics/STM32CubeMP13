/**
  * @brief  Register component IO bus
  * @param  Component object pointer
  * @retval Component status
  */
int32_t GC2145_RegisterBusIO(GC2145_Object_t *pObj, GC2145_IO_t *pIO)
{
  int32_t ret;

  if (pObj == NULL)
  {
    ret = GC2145_ERROR;
  }
  else
  {
    pObj->IO.Init      = pIO->Init;
    pObj->IO.DeInit    = pIO->DeInit;
    pObj->IO.Address   = pIO->Address;
    pObj->IO.WriteReg  = pIO->WriteReg;
    pObj->IO.ReadReg   = pIO->ReadReg;
    pObj->IO.GetTick   = pIO->GetTick;

    pObj->Ctx.ReadReg  = GC2145_ReadRegWrap;
    pObj->Ctx.WriteReg = GC2145_WriteRegWrap;
    pObj->Ctx.handle   = pObj;

    if (pObj->IO.Init != NULL)
    {
      ret = pObj->IO.Init();
    }
    else
    {
      ret = GC2145_ERROR;
    }
  }

  return ret;
}

/**
  * @brief  Initializes the GC2145 CAMERA component.
  * @param  pObj  pointer to component object
  * @param  Resolution  Camera resolution
  * @param  PixelFormat pixel format to be configured
  * @retval Component status
  */
int32_t GC2145_Init(GC2145_Object_t *pObj, uint32_t Resolution, uint32_t PixelFormat)
{
  int32_t ret = GC2145_OK;
  struct regval *reg_list = NULL;
  uint32_t size;

  if (pObj->IsInitialized == 0U)
  {
    /* Check if resolution and format supported */
    if (((Resolution != GC2145_R640x480) && (Resolution != GC2145_R1600x1200)) ||
        ((PixelFormat != GC2145_PF_RGB565) && (PixelFormat != GC2145_PF_YUV422)))
    {
      ret = GC2145_ERROR;
    }
    else
    {

      if ((PixelFormat == GC2145_PF_RGB565) && (Resolution == GC2145_R1600x1200))
      {
        reg_list = (struct regval *) &fmt_rgb565_res_1600x1200;
        size = ARRAY_SIZE(fmt_rgb565_res_1600x1200);
      }
      else if ((PixelFormat == GC2145_PF_RGB565) && (Resolution == GC2145_R640x480))
      {
        reg_list = (struct regval *) &fmt_rgb565_res_640x480;
        size = ARRAY_SIZE(fmt_rgb565_res_640x480);
      }
      else if ((PixelFormat == GC2145_PF_YUV422) && (Resolution == GC2145_R1600x1200))
      {
        reg_list = (struct regval *) &fmt_yuv_res_1600x1200;
        size = ARRAY_SIZE(fmt_yuv_res_1600x1200);
      }
      else
      {
        ret = GC2145_ERROR;
      }

      if (ret != GC2145_ERROR)
      {
        if (GC2145_WriteTable(pObj, reg_list, size) != GC2145_OK)
        {
          ret = GC2145_ERROR;
        }
      }

    }
  }

  return ret;
}

