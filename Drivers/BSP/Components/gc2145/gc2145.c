/**
  * @brief  Read the GC2145 Camera identity.
  * @param  pObj  pointer to component object
  * @param  Id    pointer to component ID
  * @retval Component status
  */
int32_t GC2145_ReadID(GC2145_Object_t *pObj, uint32_t *Id)
{
  int32_t ret;
  uint8_t low_byte;
  uint8_t upper_byte;
  uint16_t tmp = 0;

  /* Initialize I2C */
  pObj->IO.Init();

  if (gc2145_read_reg(&pObj->Ctx, 0xF0, &low_byte, 1) != GC2145_OK)
  {
    ret = GC2145_ERROR;
  }
  else
  {
    tmp = tmp << 8U;

    if (gc2145_read_reg(&pObj->Ctx, 0xF1, &upper_byte, 1) != GC2145_OK)
    {
      ret = GC2145_ERROR;
    }
    else
    {
      *Id = (low_byte << 8U) | upper_byte;
      ret = GC2145_OK;
    }
  }

  /* Component status */
  return ret;
}

