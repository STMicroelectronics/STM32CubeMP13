static int32_t GC2145_WriteTable(GC2145_Object_t *pObj, const struct regval *regs, uint32_t size)
{
  uint32_t index;
  int32_t ret = GC2145_OK;

  /* Set registers */
  for (index = 0; index < size ; index++)
  {
    if (ret != GC2145_ERROR)
    {
      if (gc2145_write_reg(&pObj->Ctx, regs[index].addr, (uint8_t *) & (regs[index].val), 1) != GC2145_OK)
      {
        ret = GC2145_ERROR;
      }
    }
  }
  return ret;
}

